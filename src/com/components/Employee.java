    private void txt_searchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyTyped
        try {
            String query = "select * from  employee where e_name=? ";
            pst = connection.prepareStatement(query);
            pst.setString(1, txt_search.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("e_id");
                txt_id.setText(add1);
                String add2 = rs.getString("e_name");
                txt_name.setText(add2);
                String add3 = rs.getString("age");
                txt_age.setText(add3);
                String add4 = rs.getString("citizenship_id");
                txt_citizen.setText(add4);
                String add5 = rs.getString("phone_number");
                txt_phone.setText(add5);
                String add6 = rs.getString("post");
                txt_post.setText(add6);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

// TODO add your handling code here:
    }//GEN-LAST:event_txt_searchKeyTyped

