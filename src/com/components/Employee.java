    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try {
            String query = "insert into employee (e_id,e_name,age,citizenship_id,phone_number,post) values (?,?,?,?,?,?)";
            pst = connection.prepareStatement(query);
            pst.setString(1, txt_id.getText());
            pst.setString(2, txt_name.getText());
            pst.setString(3, txt_age.getText());
            pst.setString(4, txt_citizen.getText());
            pst.setString(5, txt_phone.getText());
            pst.setString(6, txt_post.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Record Succesful !!!");
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        Update_table();

// TODO add your handling code here:
    }//GEN-LAST:event_addActionPerformed

