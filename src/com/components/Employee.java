    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed

        try {
            String value1 = txt_id.getText();
            String value2 = txt_name.getText();
            String value3 = txt_age.getText();
            String value4 = txt_citizen.getText();
            String value5 = txt_phone.getText();
            String value6 = txt_post.getText();
            String query = "update employee set e_id='" + value1 + "' ,e_name='" + value2 + "',age='" + value3 + "',citizenship_id ='" + value4 + "',phone_number='" + value5 + "',post='" + value6 + "' where e_id='" + value1 + "' ";
            pst = connection.prepareStatement(query);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Record Updated!!!");
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        Update_table();
    }//GEN-LAST:event_updateActionPerformed

